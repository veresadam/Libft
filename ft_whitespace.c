#include "libft.h"

int		ft_whitespace(char *s)
{
	int count;
	int i;

	count = 0;
	i = 0;
	while (s[i])
	{
		if (s[i] == ' ' || s[i] == '\n' || s[i] == '\t')
			count++;
		i++;
	}
	return (count);
}
