#include "libft.h"

char	*ft_strchr(char const *str, int c)
{
	char	*s1;

	s1 = (char *)str;
	while (*s1 != c)
	{
		if (*s1 == '\0')
			return (0);
		s1++;
	}
	return (s1);
}
