#include "libft.h"

int		ft_countwords(const char *s, char c)
{
	int		w;
	int		i;

	i = 0;
	w = 0;
	while (s[i] != 0)
	{
		while (s[i] == c && s[i] != 0)
			i++;
		if (s[i] != c && s[i] != 0)
			w++;
		while (s[i] != c && s[i] != 0)
			i++;
	}
	return (w);
}
